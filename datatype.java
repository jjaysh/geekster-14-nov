package ASSIGNMENT;
public class datatype {
	public static void main(String[] args) {
		@SuppressWarnings("resource")
		int i=10;
		double d=125.12453465;
		char c='M';
		
		//integer value
		System.out.println("Integer : "+i);
		
		//double value
		System.out.println("Double : "+d);
		
		//character value
		System.out.println("Character : "+c);
		
}
}
